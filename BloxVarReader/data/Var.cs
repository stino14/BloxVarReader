using System;
using System.Collections.Generic;
using System.Text;

namespace BloxVarReader.data
{
	public class Var
	{
		private string m_szName;
		private string m_szVarType;
		private UseType m_stUseType;
		private bool m_bAutoIndex;
		private int d_SeriesSize;
		private string m_szDefaultValue;
		private Scope m_stScope;


		public Var()
		{ }

		#region Properties
		public string Name
		{
			get { return m_szName; }
			set { m_szName = value; }
		}

		public string VarType
		{
			get { return m_szVarType; }
			set { m_szVarType = value; }
		}

		public UseType UseType
		{
			get { return m_stUseType; }
			set { m_stUseType = value; }
		}

		public bool AutoIndex
		{
			get { return m_bAutoIndex; }
			set { m_bAutoIndex = value; }
		}

		public int SeriesSize
		{
			get { return d_SeriesSize; }
			set { d_SeriesSize = value; }
		}

		public string DefaultValue
		{
			get { return m_szDefaultValue; }
			set { m_szDefaultValue = value; }
		}

		public Scope Scope
		{
			get { return m_stScope; }
			set { m_stScope = value; }
		}
		#endregion
	}
}
